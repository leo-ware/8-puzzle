from src.heuristic_logic import _h1, _h2


def test_h1():
    assert _h1([[0]]) == 0
    assert _h1([[0, 1], [3, 2]]) == 2
    assert _h1([[0, 1, 2], [3, 4, 5], [6, 7, 8]]) == 0
    assert _h1([[1, 0, 2], [3, 4, 5], [6, 7, 8]]) == 1
    assert _h1([[3, 4, 5], [0, 1, 2], [6, 7, 8]]) == 5


def test_h2():
    assert _h2([[0, 1, 2], [3, 4, 5], [6, 7, 8]]) == 0
    assert _h2([[1, 0, 2], [3, 4, 5], [6, 7, 8]]) == 1
    assert _h2([[3, 4, 5], [0, 1, 2], [6, 7, 8]]) == 5
